function defaultEmptyScoresheet(): ScoresheetLite {
  return {
    id: "",
    status: "em_andamento",
    uiState: "pre_jogo",
    currentPeriod: 0,
    periodStartedAt: null,
    homeScore: 0,
    awayScore: 0,
    homeGoalkeeperPlayerId: null,
    awayGoalkeeperPlayerId: null,
    homeGoalkeeperAvulso: null,
    awayGoalkeeperAvulso: null,
    presence: [],
    timeline: [],
    penaltyShoot: null,
    shareDocText: null
  };
}

function serialize(s: any): ScoresheetLite {
  return {
    id: s.id,
    status: s.status,
    uiState: s.uiState,
    currentPeriod: s.currentPeriod,
    periodStartedAt: s.periodStartedAt
      ? typeof s.periodStartedAt === "string"
        ? s.periodStartedAt
        : new Date(s.periodStartedAt).toISOString()
      : null,
    homeScore: s.homeScore,
    awayScore: s.awayScore,
    homeGoalkeeperPlayerId: s.homeGoalkeeperPlayerId,
    awayGoalkeeperPlayerId: s.awayGoalkeeperPlayerId,
    homeGoalkeeperAvulso: s.homeGoalkeeperAvulsoName
      ? { name: s.homeGoalkeeperAvulsoName, jersey: s.homeGoalkeeperAvulsoJersey ?? 0 }
      : null,
    awayGoalkeeperAvulso: s.awayGoalkeeperAvulsoName
      ? { name: s.awayGoalkeeperAvulsoName, jersey: s.awayGoalkeeperAvulsoJersey ?? 0 }
      : null,
    presence: (s.presenceRecords ?? s.presence ?? []).map((p: any) => ({
      playerId: p.playerId,
      jerseyNumber: p.jerseyNumber,
      present: p.present,
      absenceType: p.absenceType,
      isGoalkeeper: p.isGoalkeeper
    })),
    timeline: (s.timelineEvents ?? s.timeline ?? []).map((e: any) => ({
      id: e.id,
      type: e.type,
      clockLabel: e.clockLabel,
      playerId: e.playerId,
      teamId: e.teamId,
      secondaryPlayerId: e.secondaryPlayerId,
      period: e.period,
      autoGeneratedFromId: e.autoGeneratedFromId,
      recordedAt:
        typeof e.recordedAt === "string"
          ? e.recordedAt
          : new Date(e.recordedAt).toISOString()
    })),
    penaltyShoot: s.penaltyShoot
      ? {
          homeScore: s.penaltyShoot.homeScore,
          awayScore: s.penaltyShoot.awayScore,
          finished: s.penaltyShoot.finished,
          kicks: s.penaltyShoot.kicks.map((k: any) => ({
            id: k.id,
            teamId: k.teamId,
            playerId: k.playerId,
            kickOrder: k.kickOrder,
            converted: k.converted
          }))
        }
      : null,
    shareDocText: s.shareDocText ?? null
  };
}
