function FoulIndividualPanel({
  ss,
  home,
  away,
  limit,
  playerById
}: {
  ss: ScoresheetLite;
  home: TeamLite;
  away: TeamLite;
  limit: number;
  playerById: Map<string, PlayerLite & { teamId: string }>;
}) {
  const all = [...home.players, ...away.players];
  const counts = all.map((p) => ({
    player: p,
    count: countPlayerFoulsInMatch(ss.timeline as any, p.id)
  }));
  return (
    <details className="bg-white rounded border border-surface-100 p-3">
      <summary className="cursor-pointer font-semibold text-sm">
        Faltas por jogador (limite {limit})
      </summary>
      <ul className="text-sm mt-2 grid grid-cols-2 gap-1">
        {counts
          .filter((c) => c.count > 0)
          .map((c) => (
            <li
              key={c.player.id}
              className={
                c.count >= limit
                  ? "text-danger font-semibold"
                  : c.count >= limit - 1
                    ? "text-alert"
                    : ""
              }
            >
              {c.player.fullName}: {c.count}
            </li>
          ))}
      </ul>
    </details>
  );
}

function PenaltyView({
  ss,
  home,
  away,
  regulation,
  onRecord,
  onUndo,
  onConfirm
}: {
  ss: ScoresheetLite;
  home: TeamLite;
  away: TeamLite;
  regulation: RegulationData | null;
  onRecord: (teamId: string, playerId: string, converted: boolean) => Promise<void>;
  onUndo: () => Promise<void>;
  onConfirm: () => Promise<void>;
}) {
  const [selected, setSelected] = useState<string | null>(null);
  const kicks = ss.penaltyShoot?.kicks ?? [];
  const kicksPerTeam = regulation?.penaltyKicksPerTeam ?? 5;

  const state = computePenaltyState({
    kicks: kicks as any,
    homeTeamId: home.id,
    awayTeamId: away.id,
    kicksPerTeam
  });
  const currentTeam = state.nextTeamId === home.id ? home : away;
  const presents = ss.presence.filter(
    (p) =>
      p.present &&
      (state.nextTeamId === home.id
        ? home.players.find((x) => x.id === p.playerId)
        : away.players.find((x) => x.id === p.playerId))
  );

  return (
    <div className="px-4 py-4 space-y-4">
      <div className="bg-white rounded-lg border border-surface-100 p-4 text-center">
        <p className="text-xs text-muted">Disputa de pênaltis</p>
        <div className="text-3xl font-bold tabular-nums my-2">
          {state.homeScore} × {state.awayScore}
        </div>
        <div className="grid grid-cols-2 gap-3 mt-3">
          {[home, away].map((team) => {
            const teamKicks = kicks.filter((k) => k.teamId === team.id);
            return (
              <div key={team.id}>
                <div className="text-xs font-semibold mb-1">{team.name}</div>
                <div className="flex gap-1 justify-center">
                  {Array.from({ length: kicksPerTeam }).map((_, i) => {
                    const kick = teamKicks[i];
                    if (!kick) {
                      return (
                        <span
                          key={i}
                          className="w-5 h-5 rounded-full bg-surface-100"
                        />
                      );
                    }
                    return (
                      <span
                        key={i}
                        className={`w-5 h-5 rounded-full text-white text-xs font-bold flex items-center justify-center ${
                          kick.converted ? "bg-success" : "bg-danger"
                        }`}
                      >
                        {kick.converted ? "✓" : "✗"}
                      </span>
                    );
                  })}
                </div>
              </div>
            );
          })}
        </div>
      </div>

      {!state.finished && (
        <div className="bg-white rounded-lg border border-surface-100 p-4 space-y-3">
          <div className="text-sm">
            {state.nextKickOrder}ª cobrança — <strong>{currentTeam.name}</strong>
          </div>
          <select
            value={selected ?? ""}
            onChange={(e) => setSelected(e.target.value || null)}
            className="w-full border border-surface-100 rounded px-3 py-2"
          >
            <option value="">Selecione o cobrador…</option>
            {presents.map((p) => {
              const player = currentTeam.players.find((x) => x.id === p.playerId)!;
              return (
                <option key={p.playerId} value={p.playerId}>
                  #{p.jerseyNumber ?? "—"} {player.fullName}
                </option>
              );
            })}
          </select>
          <div className="grid grid-cols-2 gap-2">
            <button
              disabled={!selected}
              onClick={() => selected && onRecord(currentTeam.id, selected, true)}
              className="rounded bg-success text-white py-3 font-semibold disabled:opacity-40"
            >
              Converteu
            </button>
            <button
              disabled={!selected}
              onClick={() => selected && onRecord(currentTeam.id, selected, false)}
              className="rounded bg-danger text-white py-3 font-semibold disabled:opacity-40"
            >
              Errou
            </button>
          </div>
          {kicks.length > 0 && (
            <button
              onClick={onUndo}
              className="w-full rounded bg-surface-100 py-2 text-sm font-semibold"
            >
              Desfazer última cobrança
            </button>
          )}
        </div>
      )}

      {state.finished && state.winnerTeamId && (
        <div className="bg-white rounded-lg border border-surface-100 p-4 text-center space-y-3">
          <div className="text-success font-bold">
            {state.winnerTeamId === home.id ? home.name : away.name} vence nos pênaltis
          </div>
          <div className="text-sm text-muted">
            {state.homeScore} × {state.awayScore}
          </div>
          <button
            onClick={onConfirm}
            className="w-full rounded bg-primary-500 text-white py-3 font-semibold"
          >
            Confirmar resultado
          </button>
        </div>
      )}
    </div>
  );
}

