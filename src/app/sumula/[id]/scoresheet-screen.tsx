"use client";

import { useEffect, useMemo, useState } from "react";
import { useRouter } from "next/navigation";
import type { RegulationData } from "@/shared/types";
import {
  countTeamFoulsInPeriod,
  countPlayerFoulsInMatch,
  teamFoulVisualState
} from "@/domain/fouls";
import { computePenaltyState } from "@/domain/penalty";
import { deriveScore } from "@/domain/score";

interface PlayerLite {
  id: string;
  fullName: string;
  status: string;
}

interface TeamLite {
  id: string;
  name: string;
  color: string;
  players: PlayerLite[];
}

interface PresenceLite {
  playerId: string;
  jerseyNumber: number | null;
  present: boolean;
  absenceType: string | null;
  isGoalkeeper: boolean;
}

interface TimelineEventLite {
  id: string;
  type: string;
  clockLabel: string;
  playerId: string | null;
  teamId: string;
  secondaryPlayerId: string | null;
  period: number;
  autoGeneratedFromId: string | null;
  recordedAt: string;
}

interface ScoresheetLite {
  id: string;
  status: string;
  uiState: string;
  currentPeriod: number;
  periodStartedAt: string | null;
  homeScore: number;
  awayScore: number;
  homeGoalkeeperPlayerId: string | null;
  awayGoalkeeperPlayerId: string | null;
  homeGoalkeeperAvulso: { name: string; jersey: number } | null;
  awayGoalkeeperAvulso: { name: string; jersey: number } | null;
  presence: PresenceLite[];
  timeline: TimelineEventLite[];
  penaltyShoot: {
    homeScore: number;
    awayScore: number;
    finished: boolean;
    kicks: { id: string; teamId: string; playerId: string | null; kickOrder: number; converted: boolean }[];
  } | null;
  shareDocText: string | null;
}

interface Props {
  matchId: string;
  home: TeamLite;
  away: TeamLite;
  championshipName: string;
  modality: string;
  regulation: RegulationData | null;
  initialScoresheet: ScoresheetLite | null;
  refereeName: string | null;
  homeResponsible: string | null;
  awayResponsible: string | null;
  scheduledAt: string;
  venue: string | null;
}

function formatClock(seconds: number) {
  const mm = Math.floor(seconds / 60);
  const ss = seconds % 60;
  return `${String(mm).padStart(2, "0")}:${String(ss).padStart(2, "0")}`;
}

export function ScoresheetScreen(props: Props) {
  const router = useRouter();
  const [ss, setSs] = useState<ScoresheetLite>(
    props.initialScoresheet ?? defaultEmptyScoresheet()
  );
  const [tab, setTab] = useState<"timeline" | "presence" | "elenco">("timeline");
  const [errorMsg, setErrorMsg] = useState<string | null>(null);
  const [now, setNow] = useState(Date.now());
  const [absenceModal, setAbsenceModal] = useState<{ playerId: string } | null>(null);
  const [eventModal, setEventModal] = useState<EventModalState | null>(null);
  const [endPeriodModal, setEndPeriodModal] = useState(false);
  const [saveModal, setSaveModal] = useState(false);
  const [occurrenceModal, setOccurrenceModal] = useState(false);

  // Tick crônico
  useEffect(() => {
    const t = setInterval(() => setNow(Date.now()), 1000);
    return () => clearInterval(t);
  }, []);

  // SSE
  useEffect(() => {
    const es = new EventSource(`/api/sse/match/${props.matchId}`);
    const refresh = () => fetchState();
    es.addEventListener("match.event", refresh);
    es.addEventListener("match.score", refresh);
    es.addEventListener("match.status", refresh);
    es.addEventListener("match.penalty", refresh);
    es.addEventListener("match.saved", refresh);
    return () => es.close();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [props.matchId]);

  async function fetchState() {
    const res = await fetch(`/api/sumula/matches/${props.matchId}`);
    if (!res.ok) return;
    const data = await res.json();
    if (data.scoresheet) setSs(serialize(data.scoresheet));
  }

  // ===== Derivações =====
  const elapsedSec = ss.periodStartedAt
    ? Math.max(0, Math.floor((now - new Date(ss.periodStartedAt).getTime()) / 1000))
    : 0;
  const clockLabel = formatClock(elapsedSec);
  const overTime =
    !!props.regulation &&
    elapsedSec >= (props.regulation.periodDurationMin ?? 20) * 60;

  const score = useMemo(
    () => deriveScore(ss.timeline as any, props.home.id, props.away.id),
    [ss.timeline, props.home.id, props.away.id]
  );

  const homeFouls = useMemo(
    () => countTeamFoulsInPeriod(ss.timeline as any, props.home.id, ss.currentPeriod),
    [ss.timeline, props.home.id, ss.currentPeriod]
  );
  const awayFouls = useMemo(
    () => countTeamFoulsInPeriod(ss.timeline as any, props.away.id, ss.currentPeriod),
    [ss.timeline, props.away.id, ss.currentPeriod]
  );

  const homeGoalkeeperPresence = ss.presence.find(
    (p) => p.playerId === ss.homeGoalkeeperPlayerId && p.present
  );
  const awayGoalkeeperPresence = ss.presence.find(
    (p) => p.playerId === ss.awayGoalkeeperPlayerId && p.present
  );

  const playerById = useMemo(() => {
    const map = new Map<string, PlayerLite & { teamId: string }>();
    for (const p of props.home.players) map.set(p.id, { ...p, teamId: props.home.id });
    for (const p of props.away.players) map.set(p.id, { ...p, teamId: props.away.id });
    return map;
  }, [props.home, props.away]);

  // ===== Ações =====
  async function action(url: string, body?: unknown, method: "POST" | "PATCH" | "DELETE" = "POST") {
    setErrorMsg(null);
    const res = await fetch(url, {
      method,
      headers: { "Content-Type": "application/json" },
      body: body ? JSON.stringify(body) : undefined
    });
    if (!res.ok) {
      const j = await res.json().catch(() => ({}));
      setErrorMsg(j.message ?? "Erro");
    }
    await fetchState();
    return res.ok;
  }

  async function togglePresence(p: PresenceLite) {
    if (p.present) {
      // Abrir modal de ausência
      setAbsenceModal({ playerId: p.playerId });
    } else {
      await action(`/api/sumula/matches/${props.matchId}/presence`, {
        playerId: p.playerId,
        present: true,
        jerseyNumber: p.jerseyNumber ?? null
      }, "PATCH");
    }
  }

  async function setJersey(playerId: string, value: number | null) {
    await action(`/api/sumula/matches/${props.matchId}/presence`, {
      playerId,
      present: true,
      jerseyNumber: value
    }, "PATCH");
  }

  async function startPeriod() {
    await action(`/api/sumula/matches/${props.matchId}/period`, { action: "start" });
  }
  async function endPeriod() {
    await action(`/api/sumula/matches/${props.matchId}/period`, { action: "end" });
    setEndPeriodModal(false);
  }
  async function reopenPeriod() {
    await action(`/api/sumula/matches/${props.matchId}/period`, { action: "reopen" });
  }
  async function backToPeriod() {
    await action(`/api/sumula/matches/${props.matchId}/period`, { action: "back_to_period" });
  }

  async function saveScoresheet() {
    const r = await fetch(`/api/sumula/matches/${props.matchId}/save`, { method: "POST" });
    if (!r.ok) {
      const j = await r.json().catch(() => ({}));
      setErrorMsg(j.message ?? "Erro ao salvar");
    } else {
      const data = await r.json();
      // Compartilhamento nativo (RF §16.2)
      if (typeof navigator !== "undefined" && (navigator as Navigator & { share?: (d: ShareData) => Promise<void> }).share) {
        try {
          await (navigator as Navigator & { share?: (d: ShareData) => Promise<void> }).share!({
            title: `Súmula — ${props.home.name} × ${props.away.name}`,
            text: data.shareDocText,
            url: typeof window !== "undefined" ? window.location.href : ""
          });
        } catch {
          // ignored — usuário cancelou
        }
      }
      setSaveModal(false);
      await fetchState();
      router.refresh();
    }
  }

  async function setInitialGoalkeeper(side: "home" | "away", playerId?: string, avulso?: { name: string; jersey: number }) {
    await action(`/api/sumula/matches/${props.matchId}/goalkeeper`, { side, playerId, avulso }, "PATCH");
  }

  async function deleteEvent(id: string) {
    await action(`/api/sumula/matches/${props.matchId}/events/${id}`, undefined, "DELETE");
  }

  async function registerEvent(args: {
    type: string;
    teamId: string;
    playerId?: string | null;
    secondaryPlayerId?: string | null;
  }) {
    await action(`/api/sumula/matches/${props.matchId}/events`, args);
  }

  async function recordPenaltyKick(teamId: string, playerId: string, converted: boolean) {
    await action(`/api/sumula/matches/${props.matchId}/penalty`, {
      action: "record",
      teamId,
      playerId,
      converted
    });
  }
  async function undoLastPenaltyKick() {
    await action(`/api/sumula/matches/${props.matchId}/penalty`, { action: "undo" });
  }
  async function confirmPenaltyResult() {
    await action(`/api/sumula/matches/${props.matchId}/penalty`, { action: "confirm" });
  }

  async function recordOccurrence(type: string, description: string, players: string[]) {
    await action(`/api/sumula/matches/${props.matchId}/occurrence`, {
      type,
      description,
      playersInvolved: players
    });
    setOccurrenceModal(false);
  }

  // ===== Validações de UI =====
  const canStart =
    ss.uiState === "pre_jogo" &&
    !ss.presence.some((p) => p.present && (p.jerseyNumber === null || p.jerseyNumber === undefined));

  // ============================================================
  // RENDER
  // ============================================================
  return (
    <div className="max-w-md mx-auto pb-24">
      {/* Topbar */}
      <div className="bg-ink text-white text-xs px-3 py-2 flex justify-between">
        <span>
          {props.championshipName} · {props.modality}
        </span>
        <span className="bg-alert/90 px-2 py-0.5 rounded">
          {ss.uiState === "pre_jogo"
            ? "Pré-jogo"
            : ss.uiState === "periodo_em_andamento"
              ? "Em jogo"
              : ss.uiState === "intervalo"
                ? "Intervalo"
                : ss.uiState === "modal_penaltis"
                  ? "Pênaltis"
                  : ss.uiState === "em_revisao"
                    ? "Revisão"
                    : ss.uiState === "encerrado"
                      ? "Encerrada"
                      : ss.uiState}
        </span>
      </div>

      {/* Scoreboard */}
      <div className="bg-primary-700 text-white px-4 py-4 sticky top-0 z-10 shadow-md">
        <div className="text-center text-xs opacity-80 mb-1">
          {ss.currentPeriod > 0 && `${ss.currentPeriod}º período`}
        </div>
        <div className="flex items-center justify-between gap-3">
          <TeamScoreColumn
            name={props.home.name}
            color={props.home.color}
            score={score.homeScore}
          />
          <div
            className={`text-4xl font-extrabold tabular-nums ${
              overTime ? "text-danger animate-pulse" : ""
            }`}
            aria-live="polite"
          >
            {clockLabel}
          </div>
          <TeamScoreColumn
            name={props.away.name}
            color={props.away.color}
            score={score.awayScore}
            reverse
          />
        </div>
        {props.regulation && ss.uiState === "periodo_em_andamento" && (
          <div className="mt-2 h-1.5 bg-primary-900 rounded">
            <div
              className={`h-full rounded ${overTime ? "bg-danger" : "bg-primary-200"}`}
              style={{
                width: `${Math.min(
                  100,
                  (elapsedSec / (props.regulation.periodDurationMin * 60)) * 100
                )}%`
              }}
            />
          </div>
        )}
      </div>

      {/* Falta por time */}
      {props.regulation?.foulFreeKickEnabled &&
        ss.uiState === "periodo_em_andamento" && (
          <div className="grid grid-cols-2 gap-2 px-3 py-2 bg-white border-b border-surface-100">
            <FoulChip
              count={homeFouls}
              limit={props.regulation.foulFreeKickLimit}
              teamName={props.home.name}
            />
            <FoulChip
              count={awayFouls}
              limit={props.regulation.foulFreeKickLimit}
              teamName={props.away.name}
            />
          </div>
        )}

      {/* CONTEÚDO POR ESTADO */}
      {ss.uiState === "pre_jogo" && (
        <PreGameView
          home={props.home}
          away={props.away}
          presence={ss.presence}
          regulation={props.regulation}
          championshipName={props.championshipName}
          refereeName={props.refereeName}
          homeResponsible={props.homeResponsible}
          awayResponsible={props.awayResponsible}
          scheduledAt={props.scheduledAt}
          venue={props.venue}
          homeGoalkeeperPlayerId={ss.homeGoalkeeperPlayerId}
          awayGoalkeeperPlayerId={ss.awayGoalkeeperPlayerId}
          onTogglePresence={togglePresence}
          onSetJersey={setJersey}
          onSetInitialGoalkeeper={setInitialGoalkeeper}
          onStart={startPeriod}
          canStart={canStart}
        />
      )}

      {ss.uiState === "intervalo" && (
        <div className="px-4 py-6 text-center">
          <p className="text-muted mb-4">
            Intervalo · {ss.currentPeriod}º período encerrado
          </p>
          <div className="flex gap-3">
            <button
              onClick={reopenPeriod}
              className="flex-1 rounded bg-surface-100 py-3 font-semibold"
            >
              Reabrir período
            </button>
            <button
              onClick={startPeriod}
              className="flex-1 rounded bg-primary-500 text-white py-3 font-semibold"
            >
              Iniciar {ss.currentPeriod + 1}º período
            </button>
          </div>
        </div>
      )}

      {(ss.uiState === "periodo_em_andamento" || ss.uiState === "reaberto") && (
        <PeriodView
          home={props.home}
          away={props.away}
          presence={ss.presence}
          ss={ss}
          regulation={props.regulation}
          tab={tab}
          setTab={setTab}
          onTrigger={(state) => setEventModal(state)}
          onDelete={deleteEvent}
          onTogglePresence={togglePresence}
          onSetJersey={setJersey}
          onChangeGoalkeeper={(side) =>
            setEventModal({
              kind: "troca_goleiro",
              teamId: side === "home" ? props.home.id : props.away.id
            })
          }
          onEndPeriod={() => setEndPeriodModal(true)}
          playerById={playerById}
        />
      )}

      {ss.uiState === "modal_penaltis" && (
        <PenaltyView
          ss={ss}
          home={props.home}
          away={props.away}
          regulation={props.regulation}
          onRecord={recordPenaltyKick}
          onUndo={undoLastPenaltyKick}
          onConfirm={confirmPenaltyResult}
        />
      )}

      {ss.uiState === "em_revisao" && (
        <ReviewView
          ss={ss}
          home={props.home}
          away={props.away}
          championshipName={props.championshipName}
          refereeName={props.refereeName}
          homeResponsible={props.homeResponsible}
          awayResponsible={props.awayResponsible}
          scheduledAt={props.scheduledAt}
          venue={props.venue}
          playerById={playerById}
          onBack={backToPeriod}
          onSave={() => setSaveModal(true)}
          onRegisterOccurrence={() => setOccurrenceModal(true)}
        />
      )}

      {ss.uiState === "encerrado" && (
        <div className="px-4 py-6 text-center">
          <p className="text-success font-semibold mb-2">Súmula salva.</p>
          <p className="text-sm text-muted whitespace-pre-wrap text-left bg-white border border-surface-100 rounded-lg p-3 mt-4">
            {ss.shareDocText}
          </p>
        </div>
      )}

      {errorMsg && (
        <div className="fixed bottom-3 left-3 right-3 z-30 bg-danger text-white p-3 rounded-lg text-sm">
          {errorMsg}
        </div>
      )}

      {/* MODAIS */}
      {absenceModal && (
        <AbsenceModal
          onConfirm={async (absenceType) => {
            await action(
              `/api/sumula/matches/${props.matchId}/presence`,
              {
                playerId: absenceModal.playerId,
                present: false,
                absenceType
              },
              "PATCH"
            );
            setAbsenceModal(null);
          }}
          onClose={() => setAbsenceModal(null)}
          player={playerById.get(absenceModal.playerId) ?? null}
        />
      )}

      {eventModal && (
        <EventModal
          state={eventModal}
          ss={ss}
          home={props.home}
          away={props.away}
          regulation={props.regulation}
          playerById={playerById}
          onClose={() => setEventModal(null)}
          onConfirm={async (args) => {
            await registerEvent(args);
            setEventModal(null);
          }}
        />
      )}

      {endPeriodModal && (
        <ConfirmModal
          title={`Encerrar ${ss.currentPeriod}º período?`}
          message={`Minutagem atual: ${clockLabel}${
            overTime
              ? ` — ${formatClock(elapsedSec - props.regulation!.periodDurationMin * 60)} de acréscimo`
              : ""
          }. Esta ação registra o fim do período na timeline.`}
          confirmLabel="Encerrar"
          danger
          onConfirm={endPeriod}
          onCancel={() => setEndPeriodModal(false)}
        />
      )}

      {saveModal && (
        <ConfirmModal
          title="Salvar súmula?"
          message="Após salvar, apenas o organizador pode editar. Esta ação é irreversível para você."
          confirmLabel="Salvar"
          danger={false}
          onConfirm={saveScoresheet}
          onCancel={() => setSaveModal(false)}
        />
      )}

      {occurrenceModal && (
        <OccurrenceModal
          players={[...props.home.players, ...props.away.players]}
          onClose={() => setOccurrenceModal(false)}
          onConfirm={recordOccurrence}
        />
      )}
    </div>
  );
}

