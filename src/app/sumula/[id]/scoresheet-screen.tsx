function EventModal(props: {
  state: EventModalState;
  ss: ScoresheetLite;
  home: TeamLite;
  away: TeamLite;
  regulation: RegulationData | null;
  playerById: Map<string, PlayerLite & { teamId: string }>;
  onClose: () => void;
  onConfirm: (args: {
    type: string;
    teamId: string;
    playerId?: string | null;
    secondaryPlayerId?: string | null;
  }) => Promise<void>;
}) {
  const team = props.state.teamId === props.home.id ? props.home : props.away;
  const presents = props.ss.presence.filter(
    (p) => p.present && team.players.find((x) => x.id === p.playerId)
  );
  const [playerId, setPlayerId] = useState<string | null>(null);
  const [secondaryId, setSecondaryId] = useState<string | null>(null);
  const [cardType, setCardType] = useState<"cartao_amarelo" | "cartao_vermelho" | "cartao_azul">(
    "cartao_amarelo"
  );

  let title = "";
  let needsPlayer = true;
  let onSubmit: () => Promise<void>;

  if (props.state.kind === "gol") {
    title = `Gol — ${team.name}`;
    onSubmit = () =>
      props.onConfirm({ type: "gol", teamId: team.id, playerId });
  } else if (props.state.kind === "gol_contra") {
    title = `Gol contra — ${team.name}`;
    onSubmit = () =>
      props.onConfirm({ type: "gol_contra", teamId: team.id, playerId });
  } else if (props.state.kind === "cartao") {
    title = `Cartão — ${team.name}`;
    onSubmit = () =>
      props.onConfirm({ type: cardType, teamId: team.id, playerId });
  } else if (props.state.kind === "falta") {
    title = `Falta — ${team.name}`;
    needsPlayer = false;
    onSubmit = () =>
      props.onConfirm({ type: "falta", teamId: team.id, playerId });
  } else if (props.state.kind === "substituicao") {
    title = `Substituição — ${team.name}`;
    onSubmit = () =>
      props.onConfirm({
        type: "substituicao",
        teamId: team.id,
        playerId,
        secondaryPlayerId: secondaryId
      });
  } else {
    // troca_goleiro
    title = `Troca de goleiro — ${team.name}`;
    onSubmit = () =>
      props.onConfirm({ type: "troca_goleiro", teamId: team.id, playerId });
  }

  return (
    <ModalShell onClose={props.onClose}>
      <h3 className="font-semibold mb-3">{title}</h3>

      {props.state.kind === "cartao" && (
        <div className="grid grid-cols-3 gap-2 mb-3">
          <button
            onClick={() => setCardType("cartao_amarelo")}
            className={`rounded py-2 font-semibold ${
              cardType === "cartao_amarelo" ? "bg-alert text-white" : "bg-surface-100"
            }`}
          >
            Amarelo
          </button>
          <button
            onClick={() => setCardType("cartao_vermelho")}
            className={`rounded py-2 font-semibold ${
              cardType === "cartao_vermelho" ? "bg-danger text-white" : "bg-surface-100"
            }`}
          >
            Vermelho
          </button>
          {props.regulation?.cardTypes === "com_azul" && (
            <button
              onClick={() => setCardType("cartao_azul")}
              className={`rounded py-2 font-semibold ${
                cardType === "cartao_azul" ? "bg-primary-500 text-white" : "bg-surface-100"
              }`}
            >
              Azul
            </button>
          )}
        </div>
      )}

      {(needsPlayer || props.state.kind === "falta") && (
        <select
          value={playerId ?? ""}
          onChange={(e) => setPlayerId(e.target.value || null)}
          className="w-full border border-surface-100 rounded px-3 py-2 mb-3"
        >
          <option value="">
            {props.state.kind === "falta" ? "Jogador (opcional)" : "Selecione o jogador"}
          </option>
          {presents.map((p) => {
            const player = team.players.find((x) => x.id === p.playerId)!;
            return (
              <option key={p.playerId} value={p.playerId}>
                #{p.jerseyNumber ?? "—"} {player.fullName}
              </option>
            );
          })}
        </select>
      )}

      {props.state.kind === "substituicao" && (
        <select
          value={secondaryId ?? ""}
          onChange={(e) => setSecondaryId(e.target.value || null)}
          className="w-full border border-surface-100 rounded px-3 py-2 mb-3"
        >
          <option value="">Jogador que entra</option>
          {presents
            .filter((p) => p.playerId !== playerId)
            .map((p) => {
              const player = team.players.find((x) => x.id === p.playerId)!;
              return (
                <option key={p.playerId} value={p.playerId}>
                  #{p.jerseyNumber ?? "—"} {player.fullName}
                </option>
              );
            })}
        </select>
      )}

      <button
        onClick={onSubmit}
        disabled={
          (needsPlayer && !playerId) ||
          (props.state.kind === "substituicao" && !secondaryId)
        }
        className="w-full rounded bg-primary-500 text-white py-3 font-semibold disabled:opacity-40"
      >
        Confirmar
      </button>
    </ModalShell>
  );
}

function ConfirmModal({
  title,
  message,
  confirmLabel,
  danger,
  onConfirm,
  onCancel
}: {
  title: string;
  message: string;
  confirmLabel: string;
  danger: boolean;
  onConfirm: () => void;
  onCancel: () => void;
}) {
  return (
    <ModalShell onClose={onCancel}>
      <h3 className="font-semibold mb-2">{title}</h3>
      <p className="text-sm text-muted mb-4">{message}</p>
      <div className="flex gap-2">
        <button
          onClick={onCancel}
          className="flex-1 rounded bg-surface-100 py-3 font-semibold"
        >
          Cancelar
        </button>
        <button
          onClick={onConfirm}
          className={`flex-1 rounded py-3 font-semibold text-white ${
            danger ? "bg-danger" : "bg-primary-500"
          }`}
        >
          {confirmLabel}
        </button>
      </div>
    </ModalShell>
  );
}

function OccurrenceModal({
  players,
  onClose,
  onConfirm
}: {
  players: PlayerLite[];
  onClose: () => void;
  onConfirm: (type: string, description: string, players: string[]) => void;
}) {
  const [type, setType] = useState("briga");
  const [description, setDescription] = useState("");
  const [selected, setSelected] = useState<string[]>([]);
  return (
    <ModalShell onClose={onClose}>
      <h3 className="font-semibold mb-3">Registrar ocorrência</h3>
      <select
        value={type}
        onChange={(e) => setType(e.target.value)}
        className="w-full border border-surface-100 rounded px-3 py-2 mb-3"
      >
        <option value="briga">Briga</option>
        <option value="lesao">Lesão</option>
        <option value="invasao_campo">Invasão de campo</option>
        <option value="outros">Outros</option>
      </select>
      <textarea
        placeholder="Descrição"
        value={description}
        onChange={(e) => setDescription(e.target.value)}
        rows={4}
        className="w-full border border-surface-100 rounded px-3 py-2 mb-3"
      />
      <select
        multiple
        value={selected}
        onChange={(e) =>
          setSelected(Array.from(e.target.selectedOptions, (o) => o.value))
        }
        className="w-full border border-surface-100 rounded px-3 py-2 mb-3 h-28"
      >
        {players.map((p) => (
          <option key={p.id} value={p.id}>
            {p.fullName}
          </option>
        ))}
      </select>
      <button
        onClick={() => onConfirm(type, description, selected)}
        disabled={!description.trim()}
        className="w-full rounded bg-primary-500 text-white py-3 font-semibold disabled:opacity-40"
      >
        Salvar ocorrência
      </button>
    </ModalShell>
  );
}

function ModalShell({
  children,
  onClose
}: {
  children: React.ReactNode;
  onClose: () => void;
}) {
  return (
    <div className="fixed inset-0 z-30 flex items-end sm:items-center justify-center bg-black/40">
      <div className="bg-white w-full max-w-md rounded-t-2xl sm:rounded-2xl p-4">
        <div className="flex justify-end mb-1">
          <button onClick={onClose} aria-label="Fechar" className="text-muted">
            ✕
          </button>
        </div>
        {children}
      </div>
    </div>
  );
}

