function TeamScoreColumn({
  name,
  color,
  score,
  reverse
}: {
  name: string;
  color: string;
  score: number;
  reverse?: boolean;
}) {
  return (
    <div className={`flex items-center gap-2 ${reverse ? "flex-row-reverse" : ""}`}>
      <div
        className="w-8 h-8 rounded-full border-2 border-white"
        style={{ background: color }}
      />
      <div className={reverse ? "text-right" : ""}>
        <div className="text-[10px] uppercase tracking-wide opacity-80 max-w-[80px] truncate">
          {name}
        </div>
        <div className="text-2xl font-bold tabular-nums">{score}</div>
      </div>
    </div>
  );
}

function FoulChip({
  count,
  limit,
  teamName
}: {
  count: number;
  limit: number;
  teamName: string;
}) {
  const state = teamFoulVisualState(count, limit);
  const cls =
    state === "tiro_livre"
      ? "bg-danger/10 text-danger"
      : state === "alerta"
        ? "bg-alert/10 text-alert"
        : "bg-surface-100 text-muted";
  return (
    <div className={`rounded px-2 py-1 text-xs ${cls}`}>
      <span className="font-semibold">{teamName}</span>: {count} faltas
      {state === "alerta" && " · próx. = tiro livre"}
      {state === "tiro_livre" && " · TIRO LIVRE!"}
    </div>
  );
}

function PreGameView(props: {
  home: TeamLite;
  away: TeamLite;
  presence: PresenceLite[];
  regulation: RegulationData | null;
  championshipName: string;
  refereeName: string | null;
  homeResponsible: string | null;
  awayResponsible: string | null;
  scheduledAt: string;
  venue: string | null;
  homeGoalkeeperPlayerId: string | null;
  awayGoalkeeperPlayerId: string | null;
  onTogglePresence: (p: PresenceLite) => void;
  onSetJersey: (playerId: string, value: number | null) => void;
  onSetInitialGoalkeeper: (
    side: "home" | "away",
    playerId?: string,
    avulso?: { name: string; jersey: number }
  ) => void;
  onStart: () => void;
  canStart: boolean;
}) {
  return (
    <div className="px-4 py-4 space-y-4">
      <section className="bg-white rounded-lg border border-surface-100 p-4 text-sm space-y-1">
        <div className="text-muted">{props.championshipName}</div>
        <div className="font-semibold text-base">
          {props.home.name} × {props.away.name}
        </div>
        <div>{new Date(props.scheduledAt).toLocaleString("pt-BR")}</div>
        {props.venue && <div>Local: {props.venue}</div>}
        {props.refereeName && <div>Árbitro: {props.refereeName}</div>}
        {props.homeResponsible && <div>Resp. {props.home.name}: {props.homeResponsible}</div>}
        {props.awayResponsible && <div>Resp. {props.away.name}: {props.awayResponsible}</div>}
        {props.regulation && (
          <div className="text-muted">
            {props.regulation.numPeriods}× {props.regulation.periodDurationMin}min
          </div>
        )}
      </section>

      {[props.home, props.away].map((team) => {
        const isHome = team.id === props.home.id;
        const goalkeeperId = isHome ? props.homeGoalkeeperPlayerId : props.awayGoalkeeperPlayerId;
        return (
          <PresenceTeamSection
            key={team.id}
            team={team}
            presence={props.presence}
            goalkeeperId={goalkeeperId}
            onTogglePresence={props.onTogglePresence}
            onSetJersey={props.onSetJersey}
            onSelectGoalkeeper={(playerId, avulso) =>
              props.onSetInitialGoalkeeper(isHome ? "home" : "away", playerId, avulso)
            }
          />
        );
      })}

      <button
        onClick={props.onStart}
        disabled={!props.canStart}
        className="w-full rounded-lg bg-primary-500 text-white py-4 font-semibold text-lg disabled:opacity-50 disabled:cursor-not-allowed touch-48"
      >
        Iniciar 1º período
      </button>
      {!props.canStart && (
        <p className="text-xs text-danger text-center">
          Há jogadores presentes sem número de camisa.
        </p>
      )}
    </div>
  );
}

function PresenceTeamSection({
  team,
  presence,
  goalkeeperId,
  onTogglePresence,
  onSetJersey,
  onSelectGoalkeeper
}: {
  team: TeamLite;
  presence: PresenceLite[];
  goalkeeperId: string | null;
  onTogglePresence: (p: PresenceLite) => void;
  onSetJersey: (playerId: string, value: number | null) => void;
  onSelectGoalkeeper: (playerId?: string, avulso?: { name: string; jersey: number }) => void;
}) {
  const [showAvulso, setShowAvulso] = useState(false);
  const [avulsoName, setAvulsoName] = useState("");
  const [avulsoJersey, setAvulsoJersey] = useState("");

  const presents = presence.filter((p) => p.present && team.players.find((tp) => tp.id === p.playerId));

  return (
    <section className="bg-white rounded-lg border border-surface-100">
      <div
        className="px-3 py-2 text-white text-sm font-semibold rounded-t-lg flex justify-between items-center"
        style={{ background: team.color }}
      >
        <span>{team.name}</span>
        <select
          value={goalkeeperId ?? ""}
          onChange={(e) => onSelectGoalkeeper(e.target.value || undefined)}
          className="text-ink text-xs rounded px-2 py-1"
        >
          <option value="">Goleiro inicial…</option>
          {presents.map((p) => {
            const player = team.players.find((tp) => tp.id === p.playerId)!;
            return (
              <option key={p.playerId} value={p.playerId}>
                #{p.jerseyNumber ?? "—"} {player.fullName}
              </option>
            );
          })}
        </select>
      </div>

      <ul className="divide-y divide-surface-100">
        {team.players
          .filter((p) => p.status !== "inativo")
          .map((player) => {
            const rec =
              presence.find((p) => p.playerId === player.id) ?? {
                playerId: player.id,
                jerseyNumber: null,
                present: false,
                absenceType: null,
                isGoalkeeper: false
              };
            return (
              <li key={player.id} className="px-3 py-2 flex items-center gap-3">
                <input
                  inputMode="numeric"
                  pattern="[0-9]*"
                  value={rec.jerseyNumber ?? ""}
                  onChange={(e) => {
                    const v = e.target.value.replace(/\D/g, "");
                    onSetJersey(player.id, v ? Number(v) : null);
                  }}
                  className={`w-12 h-10 rounded-full text-center font-bold text-sm border-2 ${
                    rec.present && (rec.jerseyNumber === null || rec.jerseyNumber === undefined)
                      ? "border-danger"
                      : "border-surface-100"
                  }`}
                  placeholder="—"
                  disabled={!rec.present}
                />
                <div className="flex-1">
                  <div className="font-medium text-sm">{player.fullName}</div>
                  <div className="text-xs space-x-1">
                    {player.status === "suspenso" && (
                      <span className="text-danger">⚠ suspenso</span>
                    )}
                    {goalkeeperId === player.id && (
                      <span className="text-success">goleiro</span>
                    )}
                    {rec.absenceType && (
                      <span
                        className={
                          rec.absenceType === "justificada"
                            ? "text-success"
                            : "text-danger"
                        }
                      >
                        {rec.absenceType}
                      </span>
                    )}
                  </div>
                </div>
                <button
                  onClick={() => onTogglePresence(rec)}
                  className={`px-3 py-2 rounded text-xs font-semibold touch-48 ${
                    rec.present
                      ? "bg-success text-white"
                      : "bg-surface-100 text-muted"
                  } ${player.status === "suspenso" ? "ring-2 ring-danger" : ""}`}
                >
                  {rec.present ? "Presente" : "Ausente"}
                </button>
              </li>
            );
          })}
      </ul>

      <div className="p-2 border-t border-surface-100 text-xs">
        <button
          onClick={() => setShowAvulso((s) => !s)}
          className="text-primary-500 font-medium"
        >
          + Goleiro avulso
        </button>
        {showAvulso && (
          <div className="mt-2 flex gap-2">
            <input
              placeholder="Nome"
              value={avulsoName}
              onChange={(e) => setAvulsoName(e.target.value)}
              className="flex-1 border border-surface-100 rounded px-2 py-1"
            />
            <input
              inputMode="numeric"
              placeholder="#"
              value={avulsoJersey}
              onChange={(e) => setAvulsoJersey(e.target.value.replace(/\D/g, ""))}
              className="w-16 border border-surface-100 rounded px-2 py-1"
            />
            <button
              onClick={() => {
                if (avulsoName && avulsoJersey) {
                  onSelectGoalkeeper(undefined, {
                    name: avulsoName,
                    jersey: Number(avulsoJersey)
                  });
                  setShowAvulso(false);
                  setAvulsoName("");
                  setAvulsoJersey("");
                }
              }}
              className="bg-primary-500 text-white px-3 rounded font-semibold"
            >
              OK
            </button>
          </div>
        )}
      </div>
    </section>
  );
}

